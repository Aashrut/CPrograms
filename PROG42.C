//42. Write a program to print patterns as given below:
// 123456
// 12345
// 1234
// 123
// 12
// 1

#include<stdio.h>
int main()
{
  int i,j,k,n;
  printf("Enter the number : ");
  scanf("%d",&n);
  for(i=n;i>=1;i--)
  {
    for(j=1;j<=i;j++)
    {
      printf("%d",j);
    }
    printf("\n");
  }
}
